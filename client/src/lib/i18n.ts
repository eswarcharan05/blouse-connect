export type Language = keyof typeof languages;

class I18n {
  private currentLanguage: Language = 'en';

  constructor() {
    // Load saved language from localStorage
    const saved = localStorage.getItem('blouseconnect-language') as Language;
    if (saved && saved in languages) {
      this.currentLanguage = saved;
    }
  }

  setLanguage(lang: Language) {
    this.currentLanguage = lang;
    localStorage.setItem('blouseconnect-language', lang);
  }

  getCurrentLanguage() {
    return this.currentLanguage;
  }

  t(key: string): string {
    const translation = translations[this.currentLanguage][key as keyof typeof translations['en']];
    return translation || key;
  }
}

export const i18n = new I18n();

// React hook for translations
export function useTranslation() {
  return {
    t: (key: string) => i18n.t(key),
    language: i18n.getCurrentLanguage(),
    setLanguage: (lang: Language) => i18n.setLanguage(lang),
  };
}
